package com.beans;

public class Employee {
	int empId;
	String empName;
	public Employee(int empId) {
		super();
		this.empId = empId;
		this.empName = empName;
	}
	public int getEmpId() {
		return empId;
	}
	//connected to property injection approach... 
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	
	@Override
	public String toString() {
		return "Employee [empId=" + empId + ", empName=" + empName + "]";
	}
	
	public Employee() {
		System.out.println("Object of employee is getting created !!");
	}
	

}
